    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        try{
            Statement Peticion = Conexion.obtenerConexion().createStatement();
            String Consulta = "select NOMBRE,DESCRIPCION,CANTIDAD,PRECIO,FOTO from PRODUCTO where CODIGO = '" + txtCodigo.getText() + "'";
            ResultSet datos = Peticion.executeQuery(Consulta);
            while(datos.next()){
                lblNombre.setText(datos.getString(1));
                lblDescripcion.setText(datos.getString(2));
                lblCantidad.setText(datos.getString(3));
                lblPrecio.setText("US$: " + datos.getString(4));
                lblFotoProducto.setIcon(new ImageIcon(datos.getBytes(5)));
            }
            JOptionPane.showMessageDialog(null,"Se muestra informacion del Producto","Consulta Exitosa",JOptionPane.INFORMATION_MESSAGE);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Consulta Fallida: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

